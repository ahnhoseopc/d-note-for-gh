OMT17TPNADD CHAR(1 BYTE) NULL, -- TPN 약국추가
OMT17NSSIGN CHAR(6 BYTE) NULL, -- ORDER 확인간호사
OMT17INJDEPT CHAR(1 BYTE) NULL, -- ORDER 시행장소(외래)
OMT17CHKFLG CHAR(1 BYTE) NULL, -- 소견서유무
OMT17JLCD CHAR(5 BYTE) NULL, -- 재료코드(검체코드
OMT17KCFLAG CHAR(1 BYTE) NULL, -- 검체연결FLAG, 외래혈액FLAG
OMT17KCMTCD CHAR(5 BYTE) NULL, -- 검체CMT코드
OMT17KCDAT CHAR(8 BYTE) NULL, -- 검체채취일자
OMT17KCTM CHAR(8 BYTE) NULL, -- 검체채취시간
OMT17BUICD VARCHAR2(60 BYTE) NULL, -- 부위코드
OMT17LPORTFL CHAR(2 BYTE) NULL, -- 검사PORTABLE
OMT17FLWCD CHAR(1 BYTE) NULL, -- FOLLOW-UP FLAG 
OMT17PORTFL CHAR(1 BYTE) NULL, -- PORTABLE FLAG
OMT17MRCD1 CHAR(5 BYTE) NULL, -- MRI검사항목코드1
OMT17MRCD2 CHAR(5 BYTE) NULL, -- MRI검사항목코드2
OMT17MRCD3 CHAR(5 BYTE) NULL, -- MRI검사항목코드3
OMT17POS CHAR(5 BYTE) NULL, -- 위치
OMT17TIME CHAR(5 BYTE) NULL, -- 산소 및 마취시간
OMT17O2USE NUMBER(3) NULL, -- 분당산소사용량
OMT17DEPT CHAR(5 BYTE) NULL, -- 재고부서
OMT17UDEPT CHAR(5 BYTE) NULL, -- 사용부서
OMT17TWSCD NUMBER(2) NULL, -- 2지구분
OMT17ABMFLAG CHAR(1 BYTE) NULL, -- 알부민제어FLAG
OMT17EDISPMCD CHAR(1 BYTE) NULL, -- EDI검체코드
OMT17WWCD CHAR(1 BYTE) NULL, -- 원외처방적용구분코드
OMT17WDAY NUMBER(2) NULL, -- 원외처방유호일수
OMT17WEXPCD CHAR(2 BYTE) NULL, -- 원외처방예외사항코드
OMT17WFLAG CHAR(1 BYTE) NULL, -- 추가/삭제
OMT17WCHGN CHAR(1 BYTE) NULL, -- 대체불가
OMT17WSNM CHAR(15 BYTE) NULL, -- 단말명
OMT17RQCMT VARCHAR2(200 BYTE) NULL, -- 의뢰CMT
OMT17PDAY CHAR(2 BYTE) NULL, --
OMT17ODRDV VARCHAR2(2 BYTE) NULL, --
OMT17SABUN VARCHAR2(6 BYTE) NULL, --
OMT17SEQNO NUMBER NULL --
);

