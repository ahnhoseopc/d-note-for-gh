DROP TABLE JAIN_OCS.SRDRPT04 ;

CREATE TABLE JAIN_OCS.SRDRPT04 (
SRD04STATUS CHAR(2 BYTE) NULL, -- STATUS
SRD04IDNOA CHAR(7 BYTE) NOT NULL, -- 환자번호
SRD04IDNOB CHAR(1 BYTE) NOT NULL, -- 타급종구분
SRD04LWDAT CHAR(8 BYTE) NOT NULL, -- 래원/입원일
SRD04IOCD CHAR(1 BYTE) NULL, -- I/O구분
SRD04ODRDAT CHAR(8 BYTE) NOT NULL, -- ORDER일자
SRD04ODRNO NUMBER(5) NOT NULL, -- ORDER일련번호
SRD04SUBNO NUMBER(3) NOT NULL, -- SUBNO
SRD04SLPNO CHAR(3 BYTE) NOT NULL, -- 전표번호
SRD04RDSPTH CHAR(6 BYTE) NULL, -- 판독교수코드
SRD04RDDOCCD CHAR(6 BYTE) NULL, -- 판독의
SRD04RDCD CHAR(4 BYTE) NULL, -- 판독CODE
SRD04FNCD CHAR(2 BYTE) NULL, -- 판독완료FLAG
SRD04RDDAT CHAR(8 BYTE) NULL, -- 판독일자
SRD04RDTIME CHAR(8 BYTE) NULL, -- 판독시간
SRD04AGE NUMBER(3) NULL, -- 나이
SRD04FIND VARCHAR2(4000 BYTE) NULL, -- FINDING
SRD04IMP VARCHAR2(4000 BYTE) NULL, -- IMPRESSION
SRD04ACCESSNUMBER VARCHAR2(16 BYTE) NULL -- 팍스키
);

ALTER TABLE JAIN_OCS.SRDRPT04 ADD CONSTRAINT PK_SRDRPT04
PRIMARY KEY (SRD04IDNOA, SRD04IDNOB, SRD04LWDAT, SRD04ODRDAT, SRD04ODRNO, SRD04SLPNO, SRD04SUBNO);


