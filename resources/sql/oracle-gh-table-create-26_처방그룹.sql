OMT16GRP4 CHAR(100 BYTE) NOT NULL, -- PROTOCAL GROUP 명4
OMT16PTNO CHAR(20 BYTE) NULL, -- PROTOCAL NO
OMT16PTCMT VARCHAR2(150 BYTE) NULL, -- PROTOCAL 명찰
OMT16SABUN VARCHAR2(6 BYTE) NULL, --
OMT16SORTNO NUMBER(5) NULL, --
OMT16SLPNO VARCHAR2(10 BYTE) NULL --
